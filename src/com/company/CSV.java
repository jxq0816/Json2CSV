public class CSV {
    private String column;
    private String postTile;
    private String originalUrl;
    private String sourceHost;
    private String screenName;
    private String text;
    private String createAt;
    private String productType;
    private int cmStarLevel;
    private float satisfaction;

    public String getColumn() {
        return column;
    }

    public void setColumn(String column) {
        this.column = column;
    }

    public String getPostTile() {
        return postTile;
    }

    public void setPostTile(String postTile) {
        this.postTile = postTile;
    }

    public String getOriginalUrl() {
        return originalUrl;
    }

    public void setOriginalUrl(String originalUrl) {
        this.originalUrl = originalUrl;
    }

    public String getSourceHost() {
        return sourceHost;
    }

    public void setSourceHost(String sourceHost) {
        this.sourceHost = sourceHost;
    }

    public String getScreenName() {
        return screenName;
    }

    public void setScreenName(String screenName) {
        this.screenName = screenName;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getCreateAt() {
        return createAt;
    }

    public void setCreateAt(String createAt) {
        this.createAt = createAt;
    }

    public String getProductType() {
        return productType;
    }

    public void setProductType(String productType) {
        this.productType = productType;
    }

    public int getCmStarLevel() {
        return cmStarLevel;
    }

    public void setCmStarLevel(int cmStarLevel) {
        this.cmStarLevel = cmStarLevel;
    }

    public float getSatisfaction() {
        return satisfaction;
    }

    public void setSatisfaction(float satisfaction) {
        this.satisfaction = satisfaction;
    }
}
